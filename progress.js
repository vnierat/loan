// HTML5
window.addEventListener("load", function(){
function move() {
    var elem = document.getElementById("html5"); 
    var width = 0;
    var id = setInterval(frame, 0);
    function frame() {
        if (width >= 90) {
            clearInterval(id);
        } else {
            width++; 
            elem.style.width = width + '%'; 
            // elem.innerHTML = width * 1 + '%';
        }
    }
}

move();
});

